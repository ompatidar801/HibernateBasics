package one2many8;

public class MobileNoDTO
{
	private int mobno;
	private int AdharNo;
	private int foreignPNO;
	public int getMobno() {
		return mobno;
	}
	public void setMobno(int mobno) {
		this.mobno = mobno;
	}
	public int getAdharNo() {
		return AdharNo;
	}
	public void setAdharNo(int adharNo) {
		AdharNo = adharNo;
	}
	public int getForeignPNO() {
		return foreignPNO;
	}
	public void setForeignPNO(int foreignPNO) {
		this.foreignPNO = foreignPNO;
	}
	
	
}
